my_weather = "brisk"
case my_weather
when 'raining'
    puts "Raindrops on roses and whiskers on kittens"
when 'snowing'
    puts "Bright copper kettles and warm woolen mittens"
when 'sunny'
    puts "These are a few of my favorite things"
when 'brisk'
    puts "When the dog bites, when the bee stings..."
when 'foggy'
    puts "When I'm feeling sad, I simply remember my favorite things"
else 
    puts "And then I don't feel so bad"
end
